<?php
//  Right Triangle------------------------------------------------------------------------------------------------------------
echo "Pattern 1: Right-angled Triangle<br>";
for ($i = 1; $i <= 5; $i++) {
    for ($j = 1; $j <= $i; $j++) {
        echo "* ";
    }
    echo "<br>";
}

//  Inverse right Triangle------------------------------------------------------------------------------------------------------------
echo "Pattern 2: Inverted Right-angled Triangle<br>";
for ($i = 5; $i >= 1; $i--) {
    for ($j = 1; $j <= $i; $j++) {
        echo "* ";
    }
    echo "<br>";
}

//pyramid------------------------------------------------------------------------------------------------------------
echo"pyramid<br>";
$rows = 5;

for ($i = 1; $i <= $rows; $i++) {
    for ($j = $rows - $i; $j > 0; $j--) {
        echo "  ";
    }

    for ($k = 1; $k <= (2 * $i - 1); $k++) {
        echo "* ";
    }

    echo "<br>";
}
//number pattern------------------------------------------------------------------------------------------------------------
echo "number pattern<br>";
for ($i = 1; $i <= $rows; $i++) {
    for ($j = 1; $j <= $i; $j++) {
        echo "$j ";
    }
    echo "<br>";
}

for($i=1;$i<=5;$i++){
    for($j=5;$j>=1;$j--){
        echo"*";
    }
    echo"<br>";
}
?>